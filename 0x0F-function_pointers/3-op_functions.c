#include "3-calc.h"

/**
 * op_add - add 2 numbers
 * @a: input
 * @b: input
 * Return: output
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub 2 numbers
 * @a: input
 * @b: input
 * Return: output
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply 2 numbers
 * @a: input
 * @b: input
 * Return: output
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides 2 numbers
 * @a: input
 * @b: input
 * Return: output
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - gets remainder od division of 2 numbers
 * @a: input
 * @b: input
 * Return: output
 */
int op_mod(int a, int b)
{
	return (a % b);
}

